package entities;

public class Filial {
	
	private Integer id_filial;
	private String nome;
	
	
	public Integer getId_filial() {
		return id_filial;
	}
	public void setId_filial(Integer id_filial) {
		this.id_filial = id_filial;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	
	

}
